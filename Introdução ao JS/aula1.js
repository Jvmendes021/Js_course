/*
Uma variável é uma estrutura de dados fundamental em programação que armazena um valor que pode ser modificado durante a execução do programa. Ela é utilizada para armazenar valores temporários ou permanentes, que podem ser usados em cálculos, decisões ou qualquer outra operação realizada pelo programa.

Em JavaScript, uma variável é declarada usando a palavra-chave "var", "let" ou "const", seguida do nome da variável, que deve ser única dentro do escopo em que é definida. Por exemplo:

var nome = "João";
let idade = 25;
const PI = 3.14;

No exemplo acima, "nome", "idade" e "PI" são variáveis que armazenam valores diferentes. A variável "nome" armazena uma string com o valor "João", a variável "idade" armazena um número inteiro com o valor 25 e a variável "PI" armazena um valor constante (ou seja, não pode ser alterado durante a execução do programa) com o valor 3.14.

É importante lembrar que as variáveis têm um escopo, ou seja, um conjunto de regras que determinam onde elas podem ser acessadas e modificadas. Em JavaScript, existem dois tipos principais de escopos: o escopo global (acessível em todo o programa) e o escopo local (acessível apenas dentro de uma função ou bloco de código).
*/