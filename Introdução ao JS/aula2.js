/* 
Funções são blocos de código que podem ser reutilizados em diferentes partes de um programa. Elas permitem que você defina uma sequência de instruções que podem ser executadas quantas vezes forem necessárias, com diferentes valores de entrada, e que podem retornar valores para serem utilizados em outras partes do código.

Em JavaScript, as funções são declaradas com a palavra-chave "function", seguida do nome da função e seus parâmetros entre parênteses. Por exemplo:
*/

function somar(a, b) {
    return a + b;
}

/*
No exemplo acima, a função "somar" recebe dois parâmetros (a e b) e retorna a soma desses valores. Para chamar a função, você pode simplesmente escrever o nome dela seguido dos valores dos parâmetros entre parênteses:
*/

var resultado = somar(2, 3); // resultado = 5


